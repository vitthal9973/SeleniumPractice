package Day5Methods;

public class MethodSimpleInterest {

	public static void main(String[] args) {
		float principle=2000, rof=6,time=3,interest;
		System.out.println("Enter The Principle : "+principle);
		System.out.println("Enter The Rate of Interest : "+rof);
		System.out.println("Enter The Time Period : "+time);
		interest=(principle*rof*time)/100;
		System.out.println("Enter The Principle : "+interest);
	}

}
